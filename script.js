function initFeaturedProductsScroller() {
  const scroller = document.querySelector('.rr-featured-grid');
  if (!scroller) return;

  const cards = Array.from(scroller.children);
  if (cards.length === 0) return;

  const dotsWrap = document.createElement('div');
  dotsWrap.className = 'rr-featured-dots';
  scroller.insertAdjacentElement('afterend', dotsWrap);

  let dots = [];
  let isDragging = false;
  let dragStartX = 0;
  let dragStartScroll = 0;

  function getCardsPerView() {
    if (window.matchMedia('(min-width: 769px)').matches) return 3;
    if (window.matchMedia('(min-width: 641px)').matches) return 2;
    return 1;
  }

  function getPageCount() {
    return Math.ceil(cards.length / getCardsPerView());
  }

  function renderDots() {
    dotsWrap.innerHTML = '';
    dots = Array.from({ length: getPageCount() }, (_, index) => {
      const dot = document.createElement('button');
      dot.type = 'button';
      dot.className = 'rr-featured-dot';
      dot.setAttribute('aria-label', 'Show featured products set ' + (index + 1));
      dot.addEventListener('click', () => {
        scrollToPage(index);
      });
      dotsWrap.appendChild(dot);
      return dot;
    });
    updateDots();
  }

  function getPageOffset(page) {
    const card = cards[page * getCardsPerView()];
    return card ? card.offsetLeft - scroller.offsetLeft : 0;
  }

  function scrollToPage(page) {
    scroller.scrollTo({ left: getPageOffset(page), behavior: 'smooth' });
  }

  function updateDots() {
    const cardWidth = cards[0].offsetWidth + parseFloat(getComputedStyle(scroller).gap || 0);
    const activePage = Math.round(scroller.scrollLeft / (cardWidth * getCardsPerView()));
    dots.forEach((dot, index) => dot.classList.toggle('is-active', index === activePage));
  }

  scroller.addEventListener('mousedown', (event) => {
    isDragging = true;
    dragStartX = event.pageX;
    dragStartScroll = scroller.scrollLeft;
    scroller.classList.add('is-dragging');
  });

  window.addEventListener('mousemove', (event) => {
    if (!isDragging) return;
    event.preventDefault();
    scroller.scrollLeft = dragStartScroll - (event.pageX - dragStartX);
  });

  window.addEventListener('mouseup', () => {
    isDragging = false;
    scroller.classList.remove('is-dragging');
  });

  scroller.addEventListener('scroll', updateDots, { passive: true });
  window.addEventListener('resize', renderDots);
  renderDots();
}

function initTestimonialSlider() {
  const slider = document.querySelector('[data-testimonial-slider]');
  if (!slider) return;

  const track = slider.querySelector('.rr-testimonial-track');
  const slides = Array.from(slider.querySelectorAll('.rr-testimonial-card'));
  const prev = slider.querySelector('[data-testimonial-prev]');
  const next = slider.querySelector('[data-testimonial-next]');
  const dotsWrap = slider.querySelector('[data-testimonial-dots]');
  if (!track || slides.length === 0 || !dotsWrap) return;

  let activePage = 0;
  let autoTimer;
  let dots = [];

  slides.forEach((slide) => {
    const quote = slide.querySelector('p');
    if (!quote) return;

    const button = document.createElement('button');
    button.type = 'button';
    button.className = 'rr-testimonial-readmore';
    button.textContent = 'Read More';
    button.addEventListener('click', () => {
      const isExpanded = slide.classList.toggle('is-expanded');
      button.textContent = isExpanded ? 'Read Less' : 'Read More';
    });
    quote.insertAdjacentElement('afterend', button);
  });

  function getCardsPerView() {
    if (window.matchMedia('(min-width: 981px)').matches) return 3;
    if (window.matchMedia('(min-width: 641px)').matches) return 2;
    return 1;
  }

  function getPageCount() {
    return Math.ceil(slides.length / getCardsPerView());
  }

  function renderDots() {
    dotsWrap.innerHTML = '';
    dots = Array.from({ length: getPageCount() }, (_, index) => {
      const dot = document.createElement('button');
      dot.type = 'button';
      dot.className = 'rr-testimonial-dot';
      dot.setAttribute('aria-label', 'Show testimonial set ' + (index + 1));
      dot.addEventListener('click', () => {
        setSlide(index);
        restartAuto();
      });
      dotsWrap.appendChild(dot);
      return dot;
    });
  }

  function setSlide(page) {
    const pageCount = getPageCount();
    activePage = (page + pageCount) % pageCount;
    const cardIndex = activePage * getCardsPerView();
    const offset = slides[cardIndex] ? slides[cardIndex].offsetLeft : 0;
    track.style.transform = 'translateX(-' + offset + 'px)';
    dots.forEach((dot, dotIndex) => dot.classList.toggle('is-active', dotIndex === activePage));
  }

  function restartAuto() {
    window.clearInterval(autoTimer);
    autoTimer = window.setInterval(() => setSlide(activePage + 1), 6000);
  }

  renderDots();

  if (prev) {
    prev.addEventListener('click', () => {
      setSlide(activePage - 1);
      restartAuto();
    });
  }

  if (next) {
    next.addEventListener('click', () => {
      setSlide(activePage + 1);
      restartAuto();
    });
  }

  window.addEventListener('resize', () => {
    const pageCount = getPageCount();
    if (dots.length !== pageCount) renderDots();
    setSlide(Math.min(activePage, pageCount - 1));
  });

  slider.addEventListener('mouseenter', () => window.clearInterval(autoTimer));
  slider.addEventListener('mouseleave', restartAuto);
  setSlide(0);
  restartAuto();
}

