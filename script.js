const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('is-visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.18 });

function observeRevealItems(scope = document) {
  const revealItems = scope.querySelectorAll('.product-card, .rr-product-card, .feature, .value-card, .contact-card, .quote-card, .rr-quality-grid > div, .rr-mini-grid > div, .rr-testimonial-card, .lux-form, .story-img, .rr-story-image');
  revealItems.forEach((item, index) => {
    if (item.dataset.revealReady) return;
    item.dataset.revealReady = 'true';
    item.style.opacity = '0';
    item.style.transform = 'translateY(22px)';
    item.style.transition = 'opacity .65s ease, transform .65s ease';
    item.style.transitionDelay = Math.min(index * 55, 220) + 'ms';
    revealObserver.observe(item);
  });
}

function normalizeFilter(filter = 'chhena') {
  const aliases = {
    all: 'chhena',
    cheenacombo: 'wedding',
    cheenasweets: 'chhena',
    gheemithai: 'ghee',
    khuaasweets: 'khoya',
    seasonalmithai: 'seasonal'
  };
  const cleanFilter = String(filter || 'chhena').replace(/^#/, '');
  const normalized = aliases[cleanFilter] || cleanFilter;
  return categoryMeta[normalized] ? normalized : 'chhena';
}

function setActiveFilter(filter) {
  filters.forEach(item => item.classList.toggle('active', item.dataset.filter === filter));
}

function updateCategoryHeading(filter) {
  const meta = categoryMeta[filter] || categoryMeta.chhena;
  const pageTitle = document.querySelector('#categoryTitle');
  const pageCopy = document.querySelector('#categoryCopy');
  const sectionTitle = document.querySelector('#categorySectionTitle');

  if (pageTitle) pageTitle.textContent = meta.title;
  if (pageCopy) pageCopy.textContent = meta.copy;
  if (sectionTitle) sectionTitle.textContent = meta.title;
  if (productGrid) document.title = meta.title + ' | Radha Rani Milk Delight';
}

function renderProducts(filter = 'chhena') {
  if (!productGrid) return;

  const activeFilter = normalizeFilter(filter);
  const visible = products.filter(product => product.type === activeFilter);
  setActiveFilter(activeFilter);
  updateCategoryHeading(activeFilter);

  productGrid.innerHTML = visible.map(product =>
    '<article class="product-card" data-type="' + product.type + '">' +
    '<img src="' + product.image + '" alt="' + product.name + '" loading="lazy">' +
    '<div class="body">' +
    '<span class="tag">' + product.tag + '</span>' +
    '<h3>' + product.name + '</h3>' +
    '<p>' + product.desc + '</p>' +
    '</div>' +
    '</article>'
  ).join('');

  observeRevealItems(productGrid);
}

function renderProductsFromLocation() {
  const params = new URLSearchParams(window.location.search);
  const requested = params.get('category') || document.body.dataset.productCategory || window.location.hash || 'chhena';
  renderProducts(requested);
}

