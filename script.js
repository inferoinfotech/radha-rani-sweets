const productGrid = document.querySelector('#productGrid');
const filters = document.querySelectorAll('.filter');
const menuBtn = document.querySelector('#menuBtn');
const mobileMenu = document.querySelector('#mobileMenu');

const style = document.createElement('style');
style.textContent = '.is-visible{opacity:1!important;transform:translateY(0)!important}';
document.head.appendChild(style);

const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('is-visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.18 });

function observeRevealItems(scope = document) {
  scope.querySelectorAll('.product-card, .rr-product-card, .feature, .value-card, .contact-card, .quote-card, .rr-quality-grid > div, .rr-mini-grid > div').forEach((item) => {
    if (item.dataset.revealReady) return;
    item.dataset.revealReady = 'true';
    item.style.opacity = '0';
    item.style.transform = 'translateY(22px)';
    item.style.transition = 'opacity .65s ease, transform .65s ease';
    revealObserver.observe(item);
  });
}

function normalizeFilter(filter = 'all') {
  const aliases = {
    '#chhena': 'chhena',
    '#ghee': 'ghee',
    '#khoya': 'khoya',
    '#dry-fruit': 'dry-fruit',
    '#seasonal': 'seasonal',
    '#wedding': 'wedding',
    cheenacombo: 'wedding',
    cheenasweets: 'chhena',
    gheemithai: 'ghee',
    khuaasweets: 'khoya',
    seasonalmithai: 'seasonal'
  };
  return aliases[filter] || aliases['#' + filter] || filter || 'all';
}

function setActiveFilter(filter) {
  filters.forEach(item => item.classList.toggle('active', item.dataset.filter === filter));
}

function renderProducts(filter = 'all') {
  if (!productGrid) return;

  const activeFilter = normalizeFilter(filter);
  const visible = activeFilter === 'all' ? products : products.filter(product => product.type === activeFilter);
  setActiveFilter(activeFilter);

  productGrid.innerHTML = visible.map(product =>
    '<article class="product-card" data-type="' + product.type + '">' +
      '<img src="' + product.image + '" alt="' + product.name + '" loading="lazy">' +
      '<div class="body">' +
        '<span class="tag">' + product.tag + '</span>' +
        '<h3>' + product.name + '</h3>' +
        '<p>' + product.desc + '</p>' +
      '</div>' +
    '</article>'
  ).join('');

  observeRevealItems(productGrid);
}

function renderProductsFromLocation() {
  const params = new URLSearchParams(window.location.search);
  const requested = params.get('category') || window.location.hash || 'all';
  renderProducts(requested);
}

filters.forEach(button => {
  button.addEventListener('click', () => {
    const nextFilter = button.dataset.filter;
    if (history.pushState) history.pushState(null, '', nextFilter === 'all' ? 'products.html' : '#' + nextFilter);
    renderProducts(nextFilter);
  });
});

if (menuBtn && mobileMenu) {
  menuBtn.addEventListener('click', () => {
    mobileMenu.classList.toggle('is-active');
    menuBtn.classList.toggle('is-open');
  });

  mobileMenu.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', () => {
      mobileMenu.classList.remove('is-active');
      menuBtn.classList.remove('is-open');
    });
  });

  const mobileDropdowns = mobileMenu.querySelectorAll('.mobile-dropdown-toggle');
  mobileDropdowns.forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      e.stopPropagation();
      const dropdown = btn.closest('.mobile-dropdown');
      if (dropdown) dropdown.classList.toggle('is-expanded');
    });
  });
}

document.querySelectorAll('.lux-form').forEach(form => {
  form.addEventListener('submit', (event) => {
    event.preventDefault();
    form.classList.add('is-submitted');
    const button = form.querySelector('button');
    if (button) button.textContent = 'Request Received';
  });
});

window.addEventListener('hashchange', renderProductsFromLocation);
renderProductsFromLocation();
observeRevealItems();
